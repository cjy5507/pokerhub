export function getCurrentDealer(): DealerCharacter {
  const index = Math.floor(Date.now() / (1000 * 60 * 60)) % DEALERS.length;
  return DEALERS[index];
}

export function getDealerPhrase(
  dealer: DealerCharacter,
  state: keyof DealerCharacter["phrases"]
): string {
  const phrases = dealer.phrases[state];
  const randomIndex = Math.floor(Math.random() * phrases.length);
  return phrases[randomIndex];
}
