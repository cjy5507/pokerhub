// ---------------------------------------------------------------------------
// Main seed function
// ---------------------------------------------------------------------------

async function seedContent() {
  console.log('Starting content seed (production database)...\n');

  // -------------------------------------------------------------------------
  // Step 1: Find admin user
  // -------------------------------------------------------------------------
  console.log('Step 1: Finding admin user...');
  const adminRows = await sql`
    SELECT id FROM users WHERE email = 'admin@openpoker.kr' LIMIT 1
  `;
  if (adminRows.length === 0) {
    console.error('ERROR: Admin user (admin@openpoker.kr) not found. Aborting.');
    await sql.end();
    process.exit(1);
  }
  const adminId = adminRows[0].id as string;
  console.log(`  Admin user found: ${adminId}\n`);

  // -------------------------------------------------------------------------
  // Step 2: Get test users
  // -------------------------------------------------------------------------
  console.log('Step 2: Finding test users...');
  const testUser1Rows = await sql`
    SELECT id FROM users WHERE email = 'test@openpoker.kr' LIMIT 1
  `;
  const testUser2Rows = await sql`
    SELECT id FROM users WHERE email = 'test2@openpoker.kr' LIMIT 1
  `;

  const testUser1Id = testUser1Rows.length > 0 ? (testUser1Rows[0].id as string) : adminId;
  const testUser2Id = testUser2Rows.length > 0 ? (testUser2Rows[0].id as string) : adminId;
  console.log(`  test@openpoker.kr: ${testUser1Id}`);
  console.log(`  test2@openpoker.kr: ${testUser2Id}\n`);

  // -------------------------------------------------------------------------
  // Step 3: Get all board IDs
  // -------------------------------------------------------------------------
  console.log('Step 3: Fetching board IDs...');
  const boards = await sql`SELECT id, slug FROM boards`;
  if (boards.length === 0) {
    console.error('ERROR: No boards found. Run seed.ts first.');
    await sql.end();
    process.exit(1);
  }
  const boardMap = new Map<string, string>(boards.map((b: any) => [b.slug, b.id]));
  console.log(`  Found ${boards.length} boards:`, [...boardMap.keys()].join(', '), '\n');

  // -------------------------------------------------------------------------
  // Step 4: Insert posts
  // -------------------------------------------------------------------------
  console.log('Step 4: Inserting posts...');

  const allPostDefs = [
    ...noticePosts,
    ...freePosts,
    ...strategyPosts,
    ...beginnerPosts,
    ...tournamentPosts,
  ];

  let insertedCount = 0;
  const insertedPostIds: Array<{ title: string; id: string }> = [];

  for (const postDef of allPostDefs) {
    const boardId = boardMap.get(postDef.board);
    if (!boardId) {
      console.warn(`  WARNING: board "${postDef.board}" not found, skipping post "${postDef.title}"`);
      continue;
    }

    const viewCount = randInt(50, 300);
    const likeCount = randInt(5, 30);
    const paragraphs = postDef.content.split('\n\n').filter(Boolean);
    const contentHtml = paragraphs.map((p) => `<p>${p.replace(/\n/g, '<br />')}</p>`).join('\n');

    const rows = await sql`
      INSERT INTO posts (
        board_id, author_id, title, content, content_html, status,
        is_pinned, view_count, like_count, comment_count, created_at, updated_at
      )
      VALUES (
        ${boardId},
        ${adminId},
        ${postDef.title},
        ${postDef.content},
        ${contentHtml},
        'published',
        ${postDef.isPinned ?? false},
        ${viewCount},
        ${likeCount},
        0,
        ${postDef.createdAt.toISOString()},
        ${postDef.createdAt.toISOString()}
      )
      ON CONFLICT DO NOTHING
      RETURNING id, title
    `;

    if (rows.length > 0) {
      insertedPostIds.push({ id: rows[0].id as string, title: rows[0].title as string });
      insertedCount++;
    }
  }

  console.log(`  ${insertedCount} posts inserted (${allPostDefs.length - insertedCount} skipped as duplicates)\n`);

  // -------------------------------------------------------------------------
  // Step 5: Update board post counts
  // -------------------------------------------------------------------------
  console.log('Step 5: Updating board post counts...');
  await sql`
    UPDATE boards
    SET post_count = (
      SELECT COUNT(*)
      FROM posts
      WHERE posts.board_id = boards.id
        AND posts.status = 'published'
    )
  `;
  console.log('  Board post counts updated\n');

  // -------------------------------------------------------------------------
  // Step 6: Insert comments
  // -------------------------------------------------------------------------
  console.log('Step 6: Inserting comments...');

  // Fetch all inserted post IDs with titles to match comment defs
  const allPostRows = await sql`
    SELECT id, title FROM posts WHERE author_id = ${adminId} AND status = 'published'
  `;
  const postTitleMap = new Map<string, string>(allPostRows.map((r: any) => [r.title as string, r.id as string]));

  let commentCount = 0;
  const commentedPostIds = new Set<string>();

  for (const commentDef of commentDefs) {
    // Find matching post by partial keyword match
    let matchedPostId: string | undefined;
    for (const [title, id] of postTitleMap.entries()) {
      if (title.includes(commentDef.postTitleKeyword)) {
        matchedPostId = id;
        break;
      }
    }

    if (!matchedPostId) {
      console.warn(`  WARNING: No post matching keyword "${commentDef.postTitleKeyword}", skipping comment`);
      continue;
    }

    const authorId = commentDef.isTestUser2 ? testUser2Id : testUser1Id;
    const commentCreatedAt = new Date(Date.now() - randInt(1, 48) * 60 * 60 * 1000);

    await sql`
      INSERT INTO comments (post_id, author_id, content, status, created_at, updated_at)
      VALUES (
        ${matchedPostId},
        ${authorId},
        ${commentDef.content},
        'published',
        ${commentCreatedAt.toISOString()},
        ${commentCreatedAt.toISOString()}
      )
    `;
    commentedPostIds.add(matchedPostId);
    commentCount++;
  }

  console.log(`  ${commentCount} comments inserted\n`);

  // Update comment_count on posts that received comments
  console.log('  Updating comment counts on posts...');
  for (const postId of commentedPostIds) {
    await sql`
      UPDATE posts
      SET comment_count = (
        SELECT COUNT(*) FROM comments
        WHERE comments.post_id = ${postId} AND comments.status = 'published'
      )
      WHERE id = ${postId}
    `;
  }
  console.log(`  comment_count updated for ${commentedPostIds.size} posts\n`);

  // -------------------------------------------------------------------------
  // Step 7: Insert threads
  // -------------------------------------------------------------------------
  console.log('Step 7: Inserting threads...');

  let threadCount = 0;

  for (const threadDef of threadDefs) {
    const authorId = threadDef.isAdmin
      ? adminId
      : threadDef.isTestUser2
        ? testUser2Id
        : testUser1Id;

    const contentHtml = `<p>${threadDef.content}</p>`;

    await sql`
      INSERT INTO threads (id, author_id, content, content_html, likes_count, replies_count, created_at, updated_at)
      VALUES (
        gen_random_uuid(),
        ${authorId},
        ${threadDef.content},
        ${contentHtml},
        ${randInt(1, 20)},
        0,
        ${threadDef.createdAt.toISOString()},
        ${threadDef.createdAt.toISOString()}
      )
    `;
    threadCount++;
  }

  console.log(`  ${threadCount} threads inserted\n`);

  // -------------------------------------------------------------------------
  // Done
  // -------------------------------------------------------------------------
  console.log('Content seed complete!');
  console.log(`  Posts:    ${insertedCount}`);
  console.log(`  Comments: ${commentCount}`);
  console.log(`  Threads:  ${threadCount}`);

  await sql.end();
  process.exit(0);
}

seedContent().catch((err) => {
  console.error('Seed error:', err);
  process.exit(1);
});
